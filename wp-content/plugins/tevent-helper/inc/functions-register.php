// Register Custom Post Type
function tevent_register_session_cpt() {
    $labels = array(
        'name'                  => _x( 'Sessions', 'Post Type General Name', 'tevent' ),
        'singular_name'         => _x( 'Session', 'Post Type Singular Name', 'tevent' ),
        'menu_name'             => __( 'Sessions', 'tevent' ),
        'name_admin_bar'        => __( 'Session', 'tevent' ),
        'archives'              => __( 'Session Archives', 'tevent' ),
        'attributes'            => __( 'Session Attributes', 'tevent' ),
        'parent_item_colon'     => __( 'Parent Session:', 'tevent' ),
        'all_items'             => __( 'All Sessions', 'tevent' ),
        'add_new_item'          => __( 'Add New Session', 'tevent' ),
        'add_new'               => __( 'Add New', 'tevent' ),
        'new_item'              => __( 'New Session', 'tevent' ),
        'edit_item'             => __( 'Edit Session', 'tevent' ),
        'update_item'           => __( 'Update Session', 'tevent' ),
        'view_item'             => __( 'View Session', 'tevent' ),
        'view_items'            => __( 'View Sessions', 'tevent' ),
        'search_items'          => __( 'Search Session', 'tevent' ),
        'not_found'             => __( 'Not found', 'tevent' ),
        'not_found_in_trash'    => __( 'Not found in Trash', 'tevent' ),
        'featured_image'        => __( 'Featured Image', 'tevent' ),
        'set_featured_image'    => __( 'Set featured image', 'tevent' ),
        'remove_featured_image' => __( 'Remove featured image', 'tevent' ),
        'use_featured_image'    => __( 'Use as featured image', 'tevent' ),
        'insert_into_item'      => __( 'Insert into session', 'tevent' ),
        'uploaded_to_this_item' => __( 'Uploaded to this item', 'tevent' ),
        'items_list'            => __( 'Sessions list', 'tevent' ),
        'items_list_navigation' => __( 'Sessions list navigation', 'tevent' ),
        'filter_items_list'     => __( 'Filter sessions list', 'tevent' ),
    );
    $args = array(
        'label'                 => __( 'Session', 'tevent' ),
        'description'           => __( 'Post Type Description', 'tevent' ),
        'labels'                => $labels,
        'supports'              => array( 'title', 'editor', 'author', ),
        'taxonomies'            => array(),
        'hierarchical'          => false,
        'public'                => true,
        'show_ui'               => true,
        'show_in_menu'          => true,
        'menu_position'         => 5,
        'menu_icon'             => 'dashicons-calendar',
        'show_in_admin_bar'     => true,
        'show_in_nav_menus'     => true,
        'can_export'            => true,
        'has_archive'           => false,       
        'exclude_from_search'   => false,
        'publicly_queryable'    => true,
        'capability_type'       => 'page',
        'show_in_rest'          => true,
    );
    register_post_type( 'session', $args );

}
