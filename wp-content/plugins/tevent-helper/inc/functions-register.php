function tevent_add_vc_params() {
    vc_add_param( 'vc_row', array(
        'type'        => 'dropdown',
        'heading'     => esc_html__( 'Box Shadow', 'tevent' ),
        'param_name'  => 'shadow',
        'value'       => array(
            esc_html__( 'None', 'tevent' )             => '',
            esc_html__( '40px Box Shadow', 'tevent' )  => '40',
            esc_html__( '100px Box Shadow', 'tevent' ) => '100',
            ),
        'description' => esc_html__( 'Add box shadow to background.', 'tevent' ),
        ) );
}
add_action( 'vc_before_init', 'tevent_add_vc_params' );

